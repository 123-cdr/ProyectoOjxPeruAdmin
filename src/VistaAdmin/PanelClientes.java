    private void tablaMostrarClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMostrarClienteMouseClicked
        // TODO add your handling code here:
        String mostrar=(String) tablaMostrarCliente.getValueAt(tablaMostrarCliente.getSelectedRow(),2);
        try {
            String ConsultaSQL="SELECT * FROM Cliente WHERE dni='"+mostrar+"'";

            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(ConsultaSQL);                    

            if(rs.next()){
                name = rs.getString("nombre");
                apellidoPa = rs.getString("apellidoP");
                apellidoMa = rs.getString("apellidoM");
                Dni = rs.getString("dni");
                Sexo = rs.getString("sexo");
                idCliente = rs.getInt("idCliente");
            }  

        } catch (Exception e) {
            System.out.println("ERROR seleccionar datos del Cliente: "+e.getMessage());
        }
        mostrarCliente = true;
    }//GEN-LAST:event_tablaMostrarClienteMouseClicked

