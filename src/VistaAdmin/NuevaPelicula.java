    void cargarComboTipoPelicula(){
        String SQL = "SELECT nombreCategoria FROM Categoria";
        try {
            PreparedStatement pst = cn.prepareStatement(SQL);
            ResultSet rs = pst.executeQuery();
            cboCategoria.addItem("Seleccione una Opcion");
            
            while(rs.next()){
                cboCategoria.addItem(rs.getString("nombreCategoria"));
            }
        } catch (Exception e) {
            System.out.println("Error en combo Categoria: " + e);
        }
    }
    
    void LimpiarCampos(){
        txtNombrePe.setText("");
        txtCantidadPe.setText("");
        txtCostoPe.setText("");
        cboCategoria.setSelectedIndex(-1);
    }
    
    void cargarPelicula(){      
        if(PanelPelicula.mostrarPelicula==true){
            txtNombrePe.setText(PanelPelicula.nombrePeli);
            txtCantidadPe.setText(""+PanelPelicula.cantidadPeli);
            txtCostoPe.setText(""+PanelPelicula.costoPeli);
            cboCategoria.setSelectedIndex(PanelPelicula.idCategoriaPelicula);
        }
        else{
            System.out.println("Es nueva pelicula");
            cboCategoria.setSelectedIndex(-1);
        }
    }

