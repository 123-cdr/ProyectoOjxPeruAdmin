    private void tablaMostrarTarjetaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMostrarTarjetaMouseClicked
        // TODO add your handling code here:
        String mostrar=(String) tablaMostrarTarjeta.getValueAt(tablaMostrarTarjeta.getSelectedRow(),1);
        try {
            String ConsultaSQL="SELECT * FROM Cliente WHERE dni='"+mostrar+"'";
            
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(ConsultaSQL);                    

            if(rs.next()){
                idTarje = rs.getInt("idTarjeta");
                idClien = rs.getInt("idCliente");
            }  
            System.out.println(idTarje);
            System.out.println(idClien);

        } catch (Exception e) {
            System.out.println("ERROR seleccionar datos de la Cliente: "+e.getMessage());
        }
        mostrarTarjeta = true;
    }//GEN-LAST:event_tablaMostrarTarjetaMouseClicked

