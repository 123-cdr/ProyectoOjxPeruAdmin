    void cargar(String valor){     
        String mostrar="SELECT * FROM Cliente c INNER JOIN Tarjeta t ON c.idTarjeta=t.idTarjeta WHERE (c.dni LIKE '%"+valor+"%' OR c.nombre LIKE '%"+valor+"%') AND t.estadoTarjeta!="+2;
        String []titulos={"Cliente","DNI","CÃ³d Tarjeta","Saldo","Fec Vencimiento"};
        String []Registros=new String[5];
        model= new DefaultTableModel(null, titulos);
        String apellido="";
        Date fec = new Date();
        
        try {
              Statement st = cn.createStatement();
              ResultSet rs = st.executeQuery(mostrar);
              while(rs.next())
              {
                  fec = rs.getDate("fechaVencimiento");
                  apellido = rs.getString("nombre") + " "+ rs.getString("apellidoP");
                  Registros[0]= apellido;                 
                  Registros[1]= rs.getString("dni");
                  Registros[2]= rs.getString("codigoTarjeta");
                  Registros[3]= rs.getString("saldoTarjeta");
                  Registros[4]= (fec.getYear()+1900)+"-"+(fec.getMonth()+1)+"-"+fec.getDate();       
                  model.addRow(Registros);
              }
              tablaMostrarTarjeta.setModel(model);
        } catch (SQLException ex) {
            System.out.println("Error en la tabla tarjeta: " + ex);
        }
    }

