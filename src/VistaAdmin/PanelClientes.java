    void cargar(String valor){     
        String mostrar="SELECT * FROM Cliente WHERE estadoCliente="+1;
        String []titulos={"Nombres","Apellidos","DNI","Sexo"};
        String []Registros=new String[4];
        model= new DefaultTableModel(null, titulos);
        String apellido="";
        try {
              Statement st = cn.createStatement();
              ResultSet rs = st.executeQuery(mostrar);
              while(rs.next())
              {
                  Registros[0]= rs.getString("nombre");
                  apellido = rs.getString("apellidoP") + " "+ rs.getString("apellidoM");
                  Registros[1]= apellido;
                  Registros[2]= rs.getString("dni");
                  Registros[3]= rs.getString("sexo");       
                  model.addRow(Registros);
              }
              tablaMostrarCliente.setModel(model);
        } catch (SQLException ex) {
            System.out.println("Error en la tabla cliente: " + ex);
        }
    }
    
